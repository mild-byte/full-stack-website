// cdc-consumer.js
const { Kafka } = require('kafkajs');
const log4js = require('log4js');

// Configure structured logging for consistency with the backend
log4js.configure({
  appenders: { console: { type: 'stdout' } },
  categories: { default: { appenders: ['console'], level: 'info' } }
});
const logger = log4js.getLogger();

// Initialize Kafka client to connect to the internal broker
const kafka = new Kafka({
  clientId: 'cdc-consumer',
  brokers: [process.env.KAFKA_BROKER || 'kafka:9092']
});

const consumer = kafka.consumer({ groupId: 'db-change-group' });

async function run() {
  // Establish connection to the Kafka cluster
  await consumer.connect();

  // Subscribe to the topic generated by Debezium
  // Topic naming convention: <topic.prefix>.<database_name>.<table_name>
  await consumer.subscribe({ topic: 'cdc.mydatabase.tasks', fromBeginning: false });

  await consumer.run({
    eachMessage: async ({ message }) => {
      // Debezium emits a tombstone record (null value) after every delete for
      // Kafka log-compaction purposes; there's no change data to process here.
      if (message.value === null) return;

      // Decode the binary message payload from Kafka
      const rawPayload = JSON.parse(message.value.toString());

      // Debezium payload contains 'before' (previous state) and 'after' (new state)
      // We extract the operation type ('c'=create, 'u'=update, 'd'=delete)
      const data = rawPayload.payload.after || rawPayload.payload.before;
      const op = rawPayload.payload.op;

      // Structured logging output for the change event
      logger.info(JSON.stringify({
        timestamp: new Date().toISOString(),
        operation: op,
        data: data
      }));
    },
  });
}

// Global error handler to restart the consumer if it crashes
run().catch(console.error);